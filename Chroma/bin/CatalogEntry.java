public class CatalogEntry {
    private String name;
    private String description;
    private String field1;
    private String field2;
    private String field3;
    private String field4;

    public CatalogEntry(String name, String description, String field1, String field2, String field3, String field4) {
        this.name = name;
        this.description = description;
        this.field1 = field1;
        this.field2 = field2;
        this.field3 = field3;
        this.field4 = field4;
    }


}
